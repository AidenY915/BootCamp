package com.loginboard.www.daodto;

public class MemberDto {
	private String id;
	private String password;
	public MemberDto(){}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
}
